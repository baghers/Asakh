function cleanAll($str)
{
   $str=trim($str);
   $str=htmlspecialchars($str,ENT_QUOTES,"UTF-8");
   $str=htmlentities($str,ENT_QUOTES,"UTF-8");
   $str=strip_tags($str);
   $str=mysql_real_escape_string($str);
   return $str;
}
